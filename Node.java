package t;

public class Node {

		private int Data;
		private Node Right;
		private Node Left;
		
		public int getData() {
			return Data;
		}
		public void setData(int data) {
			Data = data;
		}
		public Node getRight() {
			return Right;
		}
		public void setRight(Node right) {
			Right = right;
		}
		public Node getLeft() {
			return Left;
		}
		public void setLeft(Node left) {
			Left = left;
		}
		
		
}
